// Classes
class Car {
	constructor(color, type) {
		this.wheels = 4;
		this.attributes = {
			wheels: 4,
			color: color,
			type: type
		};
	}
}


var simpleCar = new Car('blue', 'classic coupe');
var simpleTruck = new Car('red', 'truck');

console.log(simpleCar);
console.log(simpleTruck);

